export const invertKeyValues = obj =>
  Object.keys(obj).reduce((acc, key) => {
    acc[obj[key]] = key;
    return acc;
  }, {});

const default_emoji = () => {
  let strMap = new Map()
  let index = 0
  let data = []
  for (let key of Object.keys(EMOJIS_DATA)) {
    strMap.set(key, EMOJIS_DATA[key])
  }
  for (let val of strMap.keys()) {
    data.push({
      key:index,
      value:val,
    });
    index++
  }

  let page0 = data.slice(0,23)
  let page1 = data.slice(23,46)
  let page2 = data.slice(46,69)
  let page3 = data.slice(69,92)
  let page4 = data.slice(92,115)

  page0.push({
    key:100,
    value:'/{del}'
  });
  page1.push({
    key:101,
    value:'/{del}'
  });
  page2.push({
    key:102,
    value:'/{del}'
  });
  page3.push({
    key:103,
    value:'/{del}'
  });
  page4.push({
    key:104,
    value:'/{del}'
  });
  return [page0, page1, page2, page3, page4]
}

